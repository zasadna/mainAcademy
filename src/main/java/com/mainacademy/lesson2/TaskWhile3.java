public class TaskWhile3 {
    public static void main(String[] args) {
        int i = 0;
        while (i<10){
            System.out.println("Iteration: "+i);
            i+=3;
        }
        System.out.println("");

        int k = 0;
        while (k<=10){
            System.out.println("Iteration: "+k);
            k+=3;
        }
        System.out.println("");

        int x = 0;
        while (x<10){
            System.out.println("Iteration: "+x);
            x++;
        }
        System.out.println("");

        int y = 10;
        while (y>0){
            System.out.println("Iteration: "+y);
            y--;
        }
        System.out.println("");

    }
}
