public class TaskFor5 {
    public static void main(String[] args) {
        for(int i=2; i<10; i+=2){
            System.out.println("Iteration: "+i);
        }
        System.out.println("");

        for(int k=8; k>0; k-=2){
            System.out.println("Iteration: "+k);
        }
        System.out.println("");
        for(int j=8; j<10; j+=3){
            System.out.println("Iteration: "+j);
        }
    }
}
